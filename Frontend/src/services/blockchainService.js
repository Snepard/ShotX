let provider;
let signer;

// Check if MetaMask is installed
if (window.ethereum) {
  provider = new ethers.BrowserProvider(window.ethereum);
} else {
  console.error("Please install MetaMask!");
}

/**
 * Connects the app to the user's MetaMask wallet.
 * @returns {string|null} The user's wallet address or null if failed.
 */

export const verifyLogin = async () => {
  if (!provider) return null;
  try {
    const response = await fetch('http://localhost:3001/auth/verify', {
      method: 'GET',
      headers: { 'Content-Type': 'application/json' },
      credentials: 'include', // Crucial for sending cookies
    });

    if (!response.ok) return null;
    
    const data = await response.json();
    if (data.loggedIn) {
      // If we are logged in, set up the signer for future use
      signer = await provider.getSigner();
      return data.address;
    }
    return null;
  } catch (error) {
    console.error("Verification failed:", error);
    return null;
  }
};

export const connectWallet = async () => {
  if (!provider) {
    alert("Please install MetaMask!");
    return null;
  }

  try {
    // 1️⃣ Request access to MetaMask
    const accounts = await provider.send("eth_requestAccounts", []);
    const address = accounts[0];

    // 2️⃣ Get the signer
    signer = await provider.getSigner();

    // 3️⃣ Get unique message from backend
    const messageRes = await fetch("http://localhost:3001/auth/message");
    const { message } = await messageRes.json();

    // 4️⃣ Prompt user to sign the message
    const signature = await signer.signMessage(message);

    // 5️⃣ Send signature to backend to verify login
    const loginRes = await fetch("http://localhost:3001/auth/login", {
      method: "POST",
      headers: { "Content-Type": "application/json" },
      body: JSON.stringify({ address, signature, message }),
    });

    if (!loginRes.ok) {
      throw new Error("Login failed");
    }

    console.log("✅ Wallet connected and signed in:", address);
    return address;

  } catch (error) {
    console.error("❌ Failed to connect wallet:", error);
    if (error.code !== "ACTION_REJECTED") {
      alert("An error occurred during connection. Please try again.");
    }
    return null;
  }
};


/**
 * Calls the smart contract to award coins based on the player's score.
 * @param {number} score The player's score from the game.
 */
export const awardShotXCoins = async (score) => {
  if (!contract || !signer) {
    alert("Please connect your wallet first.");
    return;
  }
  try {
    // Convert score to coins (e.g., 1 point = 10 coins)
    const amountToAward = ethers.parseUnits((score / 10).toString(), 18);
    
    console.log(`Awarding ${ethers.formatEther(amountToAward)} SXC...`);
    // Call the 'awardCoins' function on your smart contract
    const tx = await contract.awardCoins(await signer.getAddress(), amountToAward);
    
    // Wait for the transaction to be mined
    await tx.wait();
    console.log("Transaction successful!", tx.hash);
    alert(`Successfully awarded ${ethers.formatEther(amountToAward)} SXC!`);
  } catch (error) {
    console.error("Could not award coins:", error);
  }
};